function checkName(a) {
    if (typeof a !== 'string') {
        return 'Invalid';
    }
    else {
        let name = a.toLowerCase();
        if(name.at(-1) === 'a' || name.at(-1) === 'y' || name.at(-1) === 'i' || name.at(-1) === 'e' || name.at(-1) === 'o' || name.at(-1) === 'u' || name.at(-1) === 'w') {
            return 'Good Name';
        }     
        else {
            return 'Bad Name';
        }
    }
}

